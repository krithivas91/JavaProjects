package draw;
public class Rectangle implements Shapes

{
	int width, height;
	String name;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String toString()
	{
		return "Rectangle with width "+width+" and height "+height+" is drawn";
		
	}

}
